
public abstract class Item {
	private int posX; //atribuindo variaveis
	private int posY;
	
	public void pegar() {  //metodo generico pegar
		
	}

	public int getPosX() {  
		return posX;
	}

	public void setPosX(int posX) {
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}

}
